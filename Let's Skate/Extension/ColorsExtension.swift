import Foundation
import UIKit

extension UIColor {
    func DarkMainColor() -> UIColor {
        return UIColor.init(red: 149/255, green: 74/255, blue: 97/255, alpha: 1)
        //954A61
    }
    
    func lightMainColor() -> UIColor {
        return UIColor.init(red: 216/255, green: 205/255, blue: 208/255, alpha: 1)
        // D8CDD0
    }

}
